#include "Console.h"

namespace Core
{

// ---------------------------------------------------------------------------------------------------------------------
//
#ifdef __linux__ 
//
// ---------------------------------------------------------------------------------------------------------------------

#include <unistd.h>

// ---------------------------------------------------------------------------------------------------------------------
USIZE WriteToConsoleN(const CHAR8* pszString, USIZE uLen) noexcept
{ return write(STDOUT_FILENO, pszString, uLen); }

// ---------------------------------------------------------------------------------------------------------------------
//
#endif // !__linux__
//
// ---------------------------------------------------------------------------------------------------------------------

USIZE WriteToConsole(const CHAR8* pszString, USIZE uLen) noexcept
{ 
    USIZE uWrote = WriteToConsoleN(pszString, uLen);
    WriteToConsoleN("\n", 1);

    return uWrote;
}

} // !Core
